import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.query.Param;
import org.springframework.stereotype.Repository;

import javax.transaction.Transactional;


@Repository
@Transactional
public interface UserContractRepository extends JpaRepository<UserContract, Long>{

    @Query("SELECT c FROM UserContract c " +
            "WHERE c.userId = :userId and c.isValid = 1")
    UserContract findByUserId(@Param("userId") long userId);

    @Query("SELECT c FROM UserContract c " +
            "WHERE c.isDefault = 1 and c.isValid = 1")
    UserContract getDefault();


}
